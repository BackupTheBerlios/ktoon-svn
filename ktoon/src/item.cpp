#include <qdom.h>
#include <qstring.h>

#include "item.h"

Item::Item()
{
    name = "";
    item_kind = ITEM_UNDEFINED;
}

Item::~Item()
{

}

void Item::setName( const QString &new_name )
{
    name = new_name;
}

QString Item::getName() const
{
    return name;
}

int Item::itemKind() const
{
    return item_kind;
}
